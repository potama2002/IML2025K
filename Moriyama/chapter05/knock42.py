import pandas as pd
from transformers import pipeline

# モデル読み込み
classifier = pipeline("zero-shot-classification", model="facebook/bart-large-mnli")

# CSVファイルのパス（修正してください）
csv_path = "/Users/takerumoriyama/Library/CloudStorage/GoogleDrive-tacom394@gmail.com/マイドライブ/vsCode/IML2025K/Moriyama/JMMLU/JMMLU/japanese_history.csv"

# 列名なしとして読み込む
df = pd.read_csv(csv_path, header=None)

# 列インデックスの意味：
# 0: 問題文
# 1〜4: 選択肢A〜D
# 5: 正解（'A', 'B', 'C', 'D'）

correct = 0

for i, row in df.iterrows():
    question = row[0]
    choices = [row[1], row[2], row[3], row[4]]
    correct_label = row[5].strip()

    result = classifier(question, candidate_labels=choices)
    prediction_text = result['labels'][0]

    predicted_label = [k for k, v in zip(['A', 'B', 'C', 'D'], choices) if v == prediction_text][0]
    is_correct = predicted_label == correct_label

    print(f"Q{i+1}: {question}")
    print(f"  → モデルの解答: {predicted_label}（{prediction_text}） {'✅' if is_correct else '❌'}")
    print(f"     正解: {correct_label}\n")

    if is_correct:
        correct += 1

accuracy = correct / len(df) * 100
print(f"=== 正解数: {correct}/{len(df)} ===")
print(f"=== 正解率: {accuracy:.2f}% ===")

